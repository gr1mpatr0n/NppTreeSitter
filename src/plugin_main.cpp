// IMPORTANT: Notepad++ requires a style XML file named <DLLName>.xml in the
// plugins\Config\ directory.  If it is missing, Notepad++ refuses to load
// the lexer plugin.  We generate this file in setInfo() (called early in
// the load sequence) if it does not already exist.
